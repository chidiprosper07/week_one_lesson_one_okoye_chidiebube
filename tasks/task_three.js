let isTrue = true;   // Assigning a boolean value to the variable 'isTrue'
let isFalse = false; // Assigning a boolean value to the variable 'isFalse'

// Using logical operators to combine and manipulate the boolean values
let result1 = isTrue && isFalse;  // Logical AND
let result2 = isTrue || isFalse;  // Logical OR
let result3 = !isTrue;            // Logical NOT

// Writing if statements to perform actions based on the boolean values
if (isTrue) {
  console.log("isTrue is true. Performing action...");
  // Perform the action here
} else {
  console.log("isTrue is false. Not performing action.");
}

if (isFalse) {
  console.log("isFalse is true. Performing action...");
  // Perform the action here
} else {
  console.log("isFalse is false. Not performing action.");
}

// Printing the results
console.log("Result 1:", result1);
console.log("Result 2:", result2);
console.log("Result 3:", result3);


