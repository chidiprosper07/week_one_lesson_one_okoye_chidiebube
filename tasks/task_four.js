let numbers = [1, 2, 3, 4, 5];  // Creating an array of numbers

// Accessing and printing the value of a specific element
console.log("Value at index 2:", numbers[2]);

// Using array methods to add or remove elements
numbers.push(6);       // Adding an element to the end of the array
numbers.pop();        // Removing the last element from the array
numbers.unshift(0);   // Adding an element to the beginning of the array
numbers.shift();      // Removing the first element from the array

// Printing the updated array
console.log("Updated Array:", numbers);
